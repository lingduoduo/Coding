class Solution:
    def countCharacters(self, words, chars: str) -> int:
        res = 0
        # res = []
        for word in words:
        	worddict = collections.Counter(chars)
        	j = 0
        	while j < len(word) and worddict[word[j]]>0:
	        	worddict[word[j]] -= 1
        		j += 1
        	if j == len(word):
        		res += len(word)
        		# res.append(word)
            
        return res

